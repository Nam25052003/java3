    private void btnsendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsendActionPerformed
        // TODO add your handling code here:
        try {
            Properties properties = new Properties();
            properties.put("mail.smtp.auth", "true");
            properties.put("mail.smtp.starttls.enable", "true");
            properties.put("mail.smtp.host", "smtp.gmail.com");
            properties.put("mail.smtp.port", 587);
            properties.put("mail.smtp.ssl.protocols", "TLSv1.2");
            properties.put("mail.smtp.ssl.trust", "smtp.gmail.com");

            String username = txtusername.getText();
            String password = pwd.getText(); //tkjwtmfvujkhrmwv

            Session session = Session.getInstance(properties, new javax.mail.Authenticator() {
                protected PasswordAuthentication getPasswordAuthentication() {
                    return new PasswordAuthentication(username, password);
                }
            });


            String from, to, cc, bcc, subject, body;
            from = txtusername.getText();
            to = txtto.getText();
            cc = txtcc.getText();
            bcc = txtbcc.getText();
            subject = txtsubject.getText();
            body = txa.getText();
            Message message = new MimeMessage(session);
            
            if (chkto.isSelected()) {                
                message.setFrom(new InternetAddress(from));
                message.setRecipients(Message.RecipientType.TO, InternetAddress.parse(to));
                message.setSubject(subject);
                message.setText(body);

                // file đính kèm
                MimeBodyPart mimeBodyPart = new MimeBodyPart();
                mimeBodyPart.setContent(body, "text/plain");
                MimeMultipart mimeMultipart = new MimeMultipart();
                mimeMultipart.addBodyPart(mimeBodyPart);
                String strattach = txtattach.getText().trim();
                if (!strattach.equals("")) {
                    MimeBodyPart attachFile = new MimeBodyPart();
                    FileDataSource fds = new FileDataSource(strattach);
                    attachFile.setDataHandler(new DataHandler(fds));
                    attachFile.setFileName(fds.getName());
                    mimeMultipart.addBodyPart(attachFile);
                }
                message.setContent(mimeMultipart);

                Transport.send(message);
            }
            
            if (chkcc.isSelected()) {               
                message.setFrom(new InternetAddress(from));
                message.setRecipients(Message.RecipientType.CC, InternetAddress.parse(cc));
                message.setSubject(subject);
                message.setText(body);

                // file đính kèm
                MimeBodyPart mimeBodyPart = new MimeBodyPart();
                mimeBodyPart.setContent(body, "text/plain");
                MimeMultipart mimeMultipart = new MimeMultipart();
                mimeMultipart.addBodyPart(mimeBodyPart);
                String strattach = txtattach.getText().trim();
                if (!strattach.equals("")) {
                    MimeBodyPart attachFile = new MimeBodyPart();
                    FileDataSource fds = new FileDataSource(strattach);
                    attachFile.setDataHandler(new DataHandler(fds));
                    attachFile.setFileName(fds.getName());
                    mimeMultipart.addBodyPart(attachFile);
                }
                message.setContent(mimeMultipart);

                Transport.send(message);
            } 
            if (chkbcc.isSelected()) {      
                message.setFrom(new InternetAddress(from));
                message.setRecipients(Message.RecipientType.BCC, InternetAddress.parse(bcc));
                message.setSubject(subject);
                message.setText(body);

                // file đính kèm
                MimeBodyPart mimeBodyPart = new MimeBodyPart();
                mimeBodyPart.setContent(body, "text/plain");
                MimeMultipart mimeMultipart = new MimeMultipart();
                mimeMultipart.addBodyPart(mimeBodyPart);
                String strattach = txtattach.getText().trim();
                if (!strattach.equals("")) {
                    MimeBodyPart attachFile = new MimeBodyPart();
                    FileDataSource fds = new FileDataSource(strattach);
                    attachFile.setDataHandler(new DataHandler(fds));
                    attachFile.setFileName(fds.getName());
                    mimeMultipart.addBodyPart(attachFile);
                }
                message.setContent(mimeMultipart);

                Transport.send(message);
            } 
            JOptionPane.showMessageDialog(null, "MAIL WAS SENT SUCCESS", "MESSAGE", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        } catch (MessagingException e) {
            Logger.getLogger(SendMail.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnsendActionPerformed

