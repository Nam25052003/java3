    private void loadDataToArrayList() {
        try {
            Class.forName(driver);
            conn = DriverManager.getConnection(url, user, password);
            stm = conn.createStatement();
            String sql = "select * from student";
            rs = stm.executeQuery(sql);
            list.clear();
            
            while (rs.next()) {
                Students sv = new Students();
                sv.setReGid(rs.getInt(1));
                sv.setName(rs.getString(2));
                sv.setAddress(rs.getString(3));
                sv.setParentname(rs.getString(4));
                sv.setPhone(rs.getString(5));
                sv.setStandard(rs.getString(6));
                sv.setFees(rs.getString(7));
                list.add(sv);
            }
            conn.close();
            fillTable();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private void fillTable() {
        tableModel = (DefaultTableModel) tbl.getModel();
        tableModel.setRowCount(0);
        for (Students sv : list) {
            tableModel.addRow(new Object[]{sv.getName(), sv.getStandard()});
        }
    }
    
