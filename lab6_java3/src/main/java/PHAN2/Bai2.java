    private void insertf() {
        try {
            Class.forName(driver);
            conn = DriverManager.getConnection(url, user, password);
            String sql = "INSERT INTO student VALUES(?,?,?,?,?,?,?)";
            pst = conn.prepareStatement(sql);
            
            pst.setString(1, txtregid.getText());
            pst.setString(2, txtname.getText());
            pst.setString(3, txa.getText());
            pst.setString(4, txtparentname.getText());
            pst.setString(5, txtcontact.getText());
            pst.setString(6, (String) cbostand.getSelectedItem().toString());
            pst.setString(7, (String) cbofees.getSelectedItem().toString());
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "add success");
            conn.close();
            clearf();
            disEnablef();
            loadDataToArrayList();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private void editf() {
        enablef();
        txtregid.setEditable(false);
        btnupdate.setEnabled(true);
        btndelete.setEnabled(true);
    }
    
    private void updatef() {
        try {
            Class.forName(driver);
            conn = DriverManager.getConnection(url, user, password);
            String sql = "UPDATE student SET namee =?, addresss =?, parentname =?, phone =?, standards =?, fees =? \n"
                    + "where regid=?";
            pst = conn.prepareCall(sql);
            
            pst.setString(1, txtname.getText());
            pst.setString(2, txa.getText());
            pst.setString(3, txtparentname.getText());
            pst.setString(4, txtcontact.getText());
            pst.setString(5, (String) cbostand.getSelectedItem().toString());
            pst.setString(6, (String) cbofees.getSelectedItem().toString());
            pst.setString(7, txtregid.getText());
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "update success");
            conn.close();
            clearf();
            disEnablef();
            loadDataToArrayList();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private void nextf() {
        index++;
        if (index >= list.size()) {
            JOptionPane.showMessageDialog(this, "đang ở cuối");
            index = list.size();
            index--;
            return;
        }
        tbl.setRowSelectionInterval(index, index);
        fillToForm(index);
        disEnablef();
    }
    
    private void prev() {
        index--;
        if (index < 0) {
            JOptionPane.showMessageDialog(this, "đang ở đầu");
            index = 0;
            return;
        }
        tbl.setRowSelectionInterval(index, index);
        fillToForm(index);
        disEnablef();
    }
    
    private void deletef() {
        try {
            Class.forName(driver);
            conn = DriverManager.getConnection(url, user, password);
            String sql = "delete from student where regid=?";
            pst = conn.prepareCall(sql);
            pst.setString(1, txtregid.getText());
            pst.execute();
            
            JOptionPane.showMessageDialog(this, "delete success");
            conn.close();
            clearf();
            disEnablef();
            loadDataToArrayList();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
