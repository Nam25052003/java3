    public boolean checkNull() {
        if (txtregid.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your regid");
            return false;
        }
        if (txtname.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your name");
            return false;
        }
        if (txa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your address ");
            return false;
        }
        if (txtparentname.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your parentname ");
            return false;
        }
        if (txtcontact.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your contactNo ");
            return false;
        }
        return true;
    }
    private static final String P_regid = "\\d";
    
    public boolean checkregid() {
        Matcher matcher3 = Pattern.compile(P_regid).matcher(txtregid.getText());
        if (!matcher3.matches()) {
            JOptionPane.showMessageDialog(this, "Please enter number ");
            return false;
        }
        for (Students sv : list) {
            if (txtregid.getText().equalsIgnoreCase(String.valueOf(sv.getReGid()))) {
                JOptionPane.showMessageDialog(this, "Duplicate");
                return false;
            }
        }
        return true;
    }
    private static final String P_SDT = "^0\\d{9}";
    
    public boolean check_sdt() {
        Matcher matcher3 = Pattern.compile(P_SDT).matcher(txtcontact.getText());
        if (!matcher3.matches()) {
            JOptionPane.showMessageDialog(this, "ContactNo sai định dạng");
            return false;
        }
        return true;
    }
