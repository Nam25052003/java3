    private void btnvalidateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnvalidateActionPerformed
        // TODO add your handling code here:
        if (check_null()) {
            String str = "";
            str = "Name : " + txtname.getText() + "\n";
            str = str + "Address : " + txaaddress.getText() + "\n";
            if (rdomale.isSelected()) {
                str = str + "Sex : male \n";
            } else {
                str = str + "Sex : female \n";
            }
            str = str + "Qualification : " + cbo.getSelectedItem() + "\n";
            if (chkread.isSelected()) {
                str = str + "Hobby : reading \n";
            }
            if (chksing.isSelected()) {
                str = str + "Hobby : singing \n";
            }
            if (chkdance.isSelected()) {
                str = str + "Hobby : dancing \n";
            }
            if ((!chkread.isSelected()) && (!chksing.isSelected()) && (!chkdance.isSelected())) {
                chkorder.setSelected(true);
                str = str + "Hobby : order";
            }
            JOptionPane.showMessageDialog(this, str);
        }
    }//GEN-LAST:event_btnvalidateActionPerformed

