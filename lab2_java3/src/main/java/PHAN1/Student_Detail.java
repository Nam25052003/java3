    public boolean check_null() {
        if (txtname.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "hãy nhập tên");
            txtname.setBackground(Color.red);
            txtname.requestFocus();
            return false;
        }
        if (txaaddress.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "hãy nhập địa chỉ");
            txaaddress.setBackground(Color.red);
            txaaddress.requestFocus();
            return false;
        }
        if (buttonGroup1.getSelection() == null) {
            JOptionPane.showMessageDialog(this, "hãy chọn giới tính");
            return false;
        }

        txtname.setBackground(Color.white);
        txaaddress.setBackground(Color.white);
        return true;
    }

