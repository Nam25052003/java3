    private void btnokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnokActionPerformed
        // TODO add your handling code here:
        if (check_null()) {
            for (User user : list) {
                if (!txtusername.getText().equals(user.getUserName())) {
                    JOptionPane.showMessageDialog(this, "user không tồn tại ! hãy nhập lại");
                    txtusername.requestFocus();
                } else {
                    if (pwdcurrent.getText().equals(pwdnewpass.getText())) {
                        JOptionPane.showMessageDialog(this, "password cũ và mới giống nhau");
                        pwdcurrent.setBackground(Color.red);
                        pwdnewpass.setBackground(Color.red);
                        pwdnewpass.requestFocus();
                    } else {
                        pwdcurrent.setBackground(Color.white);
                        pwdnewpass.setBackground(Color.white);
                        if (pwdnewpass.getText().equals(txtconfirmpass.getText())) {
                            for (User user1 : list) {
                                user1.setPwdPassWord(pwdnewpass.getText());
                            }
                            JOptionPane.showMessageDialog(this, "thay đổi password thành công");
                        } else {
                            JOptionPane.showMessageDialog(this, "thay đổi password không thành công");
                        }
                    }
                }
            }
        }

    }//GEN-LAST:event_btnokActionPerformed

