    public boolean check_null() {
        if (txtusername.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "hãy nhập username");
            txtusername.setBackground(Color.red);
            txtusername.requestFocus();
            return false;
        }
        if (pwdcurrent.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "hãy nhập password");
            pwdcurrent.setBackground(Color.red);
            pwdcurrent.requestFocus();
            return false;
        }
        if (pwdnewpass.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "hãy nhập password mới");
            pwdnewpass.setBackground(Color.red);
            pwdnewpass.requestFocus();
            return false;
        }
        if (txtconfirmpass.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "hãy nhập xác nhận mất khẩu");
            txtconfirmpass.setBackground(Color.red);
            txtconfirmpass.requestFocus();
            return false;
        }

        txtusername.setBackground(Color.white);
        pwdcurrent.setBackground(Color.white);
        pwdnewpass.setBackground(Color.white);
        txtconfirmpass.setBackground(Color.white);
        return true;
    }

