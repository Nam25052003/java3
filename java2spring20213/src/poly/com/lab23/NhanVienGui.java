    public void docfiledangbuffer(String duongdan)
    {
        arrnv.clear();
        try
        {
            FileInputStream fin= new FileInputStream(duongdan);
            InputStreamReader in = new InputStreamReader(fin, "UTF-8");
            BufferedReader bfr= new BufferedReader(in);
            String docdong=bfr.readLine();
            while(docdong!=null)
            {
                NhanVien listdoc= new NhanVien();
            
                String arr[]=docdong.split(";");
                if(arr.length==4)
                {listdoc.setManv(arr[0]);
                listdoc.setTennv(arr[1]);
                listdoc.setGioitinh(Boolean.parseBoolean(arr[3]));
                listdoc.setVaitro(Integer.parseInt(arr[3]));
                arrnv.add(listdoc);
                }
                
             docdong=bfr.readLine();
            
            }
         
        bfr.close();
        in.close();
        fin.close();
        }
        
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this, "Lỗi Hệ Thống");
        }
        
    }
    
