    public boolean luufiledangbuffer(ArrayList<NhanVien> listnv, String duongdan)
    {
        try
        {
            
            FileOutputStream fos = new FileOutputStream(duongdan);
            OutputStreamWriter osw = new OutputStreamWriter(fos,"UTF-8");
            BufferedWriter bw= new BufferedWriter(osw);
            for(NhanVien nv : listnv)
            {
                String luudong=nv.getManv() + "-" + nv.getTennv()+ "-" + nv.getGioitinh() + "-" + nv.getVaitro();
                bw.write(luudong);
                bw.newLine();
                
            }
            bw.close();
            osw.close();
            fos.close();
               
                 
            
            return true;
        }
        
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(this, "Loi");
            
            
        }
        
    return false;    
    }
    
    
    public void docfiledangbuffer2(String duongdan)
    {
       arrnv.clear();
       try
       {
           FileInputStream fin = new  FileInputStream(duongdan);
           InputStreamReader in = new InputStreamReader(fin,"UTF-8");  
           BufferedReader bfr=new BufferedReader(in);
           
            String docdong=bfr.readLine();
           while(docdong!=null)
           {
               NhanVien listdoc= new NhanVien();
               String arr[]=docdong.split("-");
               if(arr.length==4)
               {
               listdoc.setManv(arr[0]);
               listdoc.setTennv(arr[1]);
               listdoc.setGioitinh(Boolean.parseBoolean(arr[2]));
               listdoc.setVaitro(Integer.parseInt(arr[3]));
               arrnv.add(listdoc);
               }
               
               docdong=bfr.readLine();
               
           }
           
           bfr.close();
           in.close();
           fin.close();
            
       }
       
      
       catch(Exception ex)
       {
       ex.printStackTrace();
       }
    }
