    ArrayList<ModalNhanVien> arrnhanvien = new ArrayList<>();
    
    public void LoadDataToArrayList()
    {
        ModalNhanVien nv = new ModalNhanVien();
        nv.setManv(txtmanv.getText());
        nv.setHoten(txthoten.getText());
        nv.setTuoi(Integer.parseInt(txttuoi.getText()));
        nv.setLuong(Double.parseDouble(txtluong.getText()));
        nv.setEmail(txtemail.getText());
        arrnhanvien.add(nv);
        
                
    }
    
    public void LoadDataArrayListToTable()
    {
        DefaultTableModel model = (DefaultTableModel) tbldanhsach.getModel();
        
        model.setRowCount(0);
        
        /*
        for(ModalNhanVien nv : arrnhanvien)
        {
            model.addRow(new Object[]{nv.getManv(),nv.getHoten(),nv.getTuoi(),nv.getEmail(),nv.getLuong() });
            
        }
        */
        
        for(ModalNhanVien nv:arrnhanvien)
        {
            Vector vec = new Vector();
            vec.add(nv.getManv());
            vec.add(nv.getHoten());
            vec.add(nv.getTuoi());
            vec.add(nv.getEmail());
            vec.add(nv.getLuong());
            model.addRow(vec);
        }
        
        
        
      
        
        
        
        
        
        
        
    }
            
