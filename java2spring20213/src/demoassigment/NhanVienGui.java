    public static boolean luudang2object(ArrayList<NhanVien> sv, String duongdan) { // lớp Mô hình student phải kết thừa từ lớp Serializable nhé.
        try {
            FileOutputStream fo = new FileOutputStream(duongdan);// đường dẫn lưu
            ObjectOutputStream objo = new ObjectOutputStream(fo); // lưu luôn đối tượng object (file ảnh), 
            objo.writeObject(sv); // ghi xuống file
            objo.close(); // đóng file
            fo.close();

            return true;
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "lỗi file, đường dẫn lưu file");
            ex.printStackTrace();
            return false;

        }
    }

