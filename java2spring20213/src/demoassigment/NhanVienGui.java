    public void ArrayListTimToTable() {
        DefaultTableModel model = (DefaultTableModel) tbldanhsach.getModel();
        model.setRowCount(0);
        for (int i = 0; i < l2.size(); i++) {
            String ma = l2.get(i).getManv();
            String hoten = l2.get(i).getHoten();
            int tuoi = l2.get(i).getTuoi();
            String email = l2.get(i).getEmail();
            double luong = l2.get(i).getLuong();

            Object[] row = new Object[]{ma, hoten, tuoi, email, luong};
            model.addRow(row);
        }
    }

