    DefaultTableModel tableModel;

    private void loadData() {
        tableModel = (DefaultTableModel) tbl.getModel();
        try {
            Class.forName(driver);
            Connection conn = DriverManager.getConnection(url, user, password);
            String sql = "select * from GRADES";
            Statement stm = conn.createStatement();
            ResultSet rs = stm.executeQuery(sql);
            tableModel.setRowCount(0);
            while (rs.next()) {
                Vector vector = new Vector();
                vector.add(rs.getString(1));
                vector.add(rs.getDouble(2));
                vector.add(rs.getDouble(3));
                vector.add(rs.getDouble(4));
                tableModel.addRow(vector);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void addNew() {
        tableModel = (DefaultTableModel) tbl.getModel();
        try {
            Class.forName(driver);
            Connection conn = DriverManager.getConnection(url, user, password);
            String sql = "INSERT INTO GRADES\n"
                    + "(ID, masv, anh, tin, gdtc)\n"
                    + "VALUES(?,?,?,?)";
            PreparedStatement pst = conn.prepareStatement(sql);                
            pst.setString(1, txtmasv.getText());           
            pst.setFloat(2, Float.parseFloat(txtanh.getText()));
            pst.setFloat(3, Float.parseFloat(txttin.getText()));
            pst.setFloat(4, Float.parseFloat(txtgdtc.getText()));
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "add success");
            loadData();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

